import java.util.*;

public class stringPractice {
    public static void main(String[] args) {
        String s = "world@123";
        System.out.println(s);

        char ch = 97;
        System.out.println(ch);

        char ch1 = 65;
          System.out.println(ch1);

          int a = 'A';
          System.out.println(a); //output is 65 due to implicit type casting
        int a1 = 'A';
        a1 = a1+5;
          System.out.println(a1);//output is 70 due to implicit type casting

    }
}
